#include <stdio.h>
int main(void){
	float r;
	float pi = 3.14;
	printf("ENTER THE VALUE OF R -> RADIUS : ");
	scanf("%f",&r);
	float area = pi * r * r;
	float circumference = 2*pi*r;
	printf("THE AREA OF THE CIRCLE OF RADIUS %f is %f \n", r, area);
	printf("THE CIRCUMFERENCE OF THE CIRCLE OF RADIUS %f is %f ", r, circumference);
	
}
